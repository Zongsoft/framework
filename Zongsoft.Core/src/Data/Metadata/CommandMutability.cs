using System;

namespace Zongsoft.Data.Metadata
{
	/// <summary>
	/// 表示数据命令变化性的枚举。
	/// </summary>
	[Flags]
	public enum CommandMutability
	{
		/// <summary>无变化，即只读。</summary>
		None = 0,

		/// <summary>删除。</summary>
		Delete = 1,

		/// <summary>新增。</summary>
		Insert = 2,

		/// <summary>更新。</summary>
		Update = 4,

		/// <summary>增改，即新增和更新。</summary>
		Upsert = 6,
	}
}
