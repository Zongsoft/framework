namespace Zongsoft.Web;

/// <summary>
/// 表示Web宿主的接口。
/// </summary>
public interface IWebHost
{
	/// <summary>获取宿主名称。</summary>
	string Name { get; }
	/// <summary>获取宿主域名。</summary>
	string Domain { get; }
	/// <summary>获取宿主端口号。</summary>
	ushort Port { get; }
	/// <summary>获取宿主根目录。</summary>
	string RootDirectory { get; }
}
